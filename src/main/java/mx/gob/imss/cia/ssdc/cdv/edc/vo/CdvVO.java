public class CdvVO {
	
	private Long cveForanea;
	private String radio;
	private String requisito;

	/**
	 * @return the requisito
	 */
	public String getRequisito() {
		return requisito;
	}

	/**
	 * @param requisito the requisito to set
	 */
	public void setRequisito(String requisito) {
		this.requisito = requisito;
	}

	/**
	 * @return the cveForanea
	 */
	public Long getCveForanea() {
		return cveForanea;
	}

	/**
	 * @param cveForanea the cveForanea to set
	 */
	public void setCveForanea(Long cveForanea) {
		this.cveForanea = cveForanea;
	}

	/**
	 * @return the radio
	 */
	public String getRadio() {
		return radio;
	}

	/**
	 * @param radio the radio to set
	 */
	public void setRadio(String radio) {
		this.radio = radio;
	}
	
	
	
	
	
	

}
