package mx.gob.imss.cia.ssdc.cdv.edc.vo;

/**
 * 
 * @author Gamaliel
 * 
 */
public class CabeceraDatosGeneralesVo {
	private String delegacion;
	private String unidad;
	private String tipoVentanilla;
	private String turno;

	/**
	 * @return the delegacion
	 */
	public String getDelegacion() {
		return delegacion;
	}

	/**
	 * @param delegacion
	 *            the delegacion to set
	 */
	public void setDelegacion(String delegacion) {
		this.delegacion = delegacion;
	}

	/**
	 * @return the unidad
	 */
	public String getUnidad() {
		return unidad;
	}

	/**
	 * @param unidad
	 *            the unidad to set
	 */
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}

	/**
	 * @return the tipoVentanilla
	 */
	public String getTipoVentanilla() {
		return tipoVentanilla;
	}

	/**
	 * @param tipoVentanilla
	 *            the tipoVentanilla to set
	 */
	public void setTipoVentanilla(String tipoVentanilla) {
		this.tipoVentanilla = tipoVentanilla;
	}

	/**
	 * @return the turno
	 */
	public String getTurno() {
		return turno;
	}

	/**
	 * @param turno
	 *            the turno to set
	 */
	public void setTurno(String turno) {
		this.turno = turno;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "cabeceraDatosGeneralesVo [delegacion=" + delegacion
				+ ", unidad=" + unidad + ", tipoVentanilla=" + tipoVentanilla
				+ ", turno=" + turno + "]";
	}
}
