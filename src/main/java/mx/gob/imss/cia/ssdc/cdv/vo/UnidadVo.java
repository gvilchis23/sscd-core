package mx.gob.imss.cia.ssdc.cdv.vo;

/**
 * @author
 * 
 */
public class UnidadVo {

	private Long clave;
	private String unidad;

	public UnidadVo() {
	}

	/**
	 * @param clave
	 * @param unidad
	 */
	public UnidadVo(Long clave, String unidad) {
		super();
		this.clave = clave;
		this.unidad = unidad;
	}

	/**
	 * @return the clave
	 */
	public Long getClave() {
		return clave;
	}

	/**
	 * @param clave
	 *            the clave to set
	 */
	public void setClave(Long clave) {
		this.clave = clave;
	}

	/**
	 * @return the unidad
	 */
	public String getUnidad() {
		return unidad;
	}

	/**
	 * @param unidad
	 *            the unidad to set
	 */
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}
}
